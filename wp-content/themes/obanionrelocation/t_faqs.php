		<h3>The Land of the Midnight Sun</h3>
		<p>Alaska gets as much daylight and darkness as anywhere else on earth over the course of a year; it's just distributed differently.</p>
		<p>Summer is a time of long days. Above the Arctic Circle, the day can be 24 hours long or months long. In Anchorage, almost 400 miles 
		   south of the circle, the summer solstice day is 19.5 hours long.</p>
		<p>Winter has much shorter days. Above the Arctic Circle, the night can last for months, although lingering twilight brightens the sky.
		   In Anchorage, the shortest day still provides 4.5 hours of daylight.</p>
		<p>Around the equinoxes in March and September, Alaska gets the same 12 hours of daylight and 12 hours of darkness the rest of the world does.</p>
		   </p>
