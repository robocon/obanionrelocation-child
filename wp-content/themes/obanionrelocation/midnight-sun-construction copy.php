		<p>We are a company whose members have many years of combined experience in the home building and real estate industries. We have strong relationships with excellent subcontractors, real estate brokers and land owners. We are able to produce a high quality product at competitive prices.</p>
		<p>Mike Anderson has been a Residential Contractor since 2008 when he and a partner formed Midnight Sun Construction, LLC. Prior to the start of Midnight Sun Construction, LLC, Mike has had many years of experience in the construction industry.</p>
		<p>Mike was born in Anchorage, Alaska in 1978. He started construction work at the age of 15 as a framing laborer. After high school, Mike worked for a large road developer building roads and runways. This experience provided knowledge of the civil end of construction as well as the impact of construction on the environment.</p>
		<p>In 2001, Mike was a job supervisor for a large framing company that was doing a large development and provided learning about production building. Mike’s attention to detail, reliability and dedication to the job landed him a superintendent position with one of Alaska’s largest home builders. In this position, Mike completed several large residential developments, which included single family homes, duplexes, multi-unit apartments and commercial buildings.</p>
